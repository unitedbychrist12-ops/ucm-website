// Mobile Navigation Toggle
const navToggle = document.getElementById('navToggle');
const navMenu = document.getElementById('navMenu');

navToggle.addEventListener('click', () => {
    navMenu.classList.toggle('active');
});

// Smooth Scrolling
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
        e.preventDefault();
        const target = document.querySelector(this.getAttribute('href'));
        if (target) {
            target.scrollIntoView({
                behavior: 'smooth',
                block: 'start'
            });
            // Close mobile menu if open
            navMenu.classList.remove('active');
        }
    });
});

// Contact Form Handling
const contactForm = document.querySelector('.contact-form');
if (contactForm) {
    contactForm.addEventListener('submit', (e) => {
        e.preventDefault();
        alert('Thank you for your message! We will get back to you soon.');
        contactForm.reset();
    });
}

// Services Form Handling
const servicesForm = document.getElementById('servicesForm');
if (servicesForm) {
    servicesForm.addEventListener('submit', (e) => {
        e.preventDefault();
        
        // Get selected services
        const selectedServices = Array.from(document.querySelectorAll('input[name="services"]:checked'))
            .map(cb => cb.value);
        
        if (selectedServices.length === 0) {
            alert('Please select at least one service you\'re interested in.');
            return;
        }
        
        alert('Thank you for your interest! Our team will contact you within 24-48 hours with more information. 80% of commissions from your service will support UCM!');
        servicesForm.reset();
    });
}

